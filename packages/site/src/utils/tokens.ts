/**
 * Catalog of known tokens, indexed by address and chainID.
 *
 * Includes token name, ticker symbol, and icon.
 */
const tokens: token[] = [...EthereumTokens];

/**
 * Get token metadata by address and chainID.
 *
 * @param addr - Contract address of the token.
 * @param chainID - Chain ID of the token.
 * @returns Token metadata, or undefined if not found.
 */
export function getToken(addr: string, chainID = 1): token | undefined {
  return tokens.find(
    (token) => token.addr === addr && token.chainID === chainID,
  );
}
